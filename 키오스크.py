# 선택한 옵션들 중에서 가격이 있는 옵션만 찾아서 메뉴 가격에 더함
def calculate_total(menu_price, selected, menu_option):
    total = menu_price
    for opt_name in selected:
        selected_value = selected[opt_name]
        if isinstance(menu_option[opt_name], dict):
            option_dict = menu_option[opt_name]
            if selected_value in option_dict:
                total += option_dict[selected_value]

    return total


# 선택한 옵션들을 하나의 문자열로 합치는 함수
def make_option_text(selected):
    option_text_list = []
    for key in selected:
        option_text_list.append(f"({key}: {selected[key]})")
    return ", ".join(option_text_list)


# 메뉴 기본 가격을 제외한 "옵션 추가 금액"만 계산하는 함수
# cart() 함수는 menu_price + option_price 구조를 그대로 쓰기 때문에,
# 옵션 금액만 따로 뽑아주는 함수가 필요하다.
def sumoption(menuprice, select, menuoption):
    totalprice = calculate_total(menuprice, select, menuoption)
    optionprice = totalprice - menuprice
    return optionprice


# 한 메뉴에 들어있는 여러 옵션을 차례대로 고르게 하는 함수
# 마지막에는 selected 딕셔너리를 돌려준다.
def select_option(menu_option, menu_name, menu_price):
    while True:
        # 옵션들을 넣기 위한 장소 만들기
        selected = {}
        # 옵션들 키
        option_keys = list(menu_option.keys())
        # 옵션들의 인덱스를 설정하기 위한 숫자
        idx = 0
        # 옵션을 전부 다 고르기 전까지 반복
        while idx < len(option_keys):
            # 옵션의 키와 인덱스속 키를 맞추기 위한 작업
            opt_name = option_keys[idx]
            opt_value = menu_option[opt_name]
            # ICE가 아닐 때 얼음/토핑 옵션은 건너뛰기
            if opt_name in ["얼음", "토핑추가", "토핑", "토핑2"]:
                if selected.get("온도") != "ICE":
                    # 다음 옵션으로 가기 위한 인덱스 상승
                    idx += 1
                    continue
            print(f"\n[{opt_name}]")
            result = choose(opt_value)
            if result == "back":
                # 온도에서 뒤로가기 누르면 메뉴 선택으로 이동
                if idx == 0:
                    print("메뉴 선택으로 돌아갑니다.")
                    print("=" * 128)
                    return "back"
                # 현재 옵션 선택값 제거
                if opt_name in selected:
                    del selected[opt_name]
                # 바로 이전 옵션으로 이동
                idx -= 1
                # 이전 옵션 값도 다시 고르게 삭제
                prev_name = option_keys[idx]
                # 옵션값이 들어 있다면 selected에서도 삭제를 한다.
                if prev_name in selected:
                    del selected[prev_name]
                continue
            selected[opt_name] = result
            idx += 1
        total_price = calculate_total(menu_price, selected, menu_option)
        return selected


